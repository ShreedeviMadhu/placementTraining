package controlStatements.decisionMaking;

public class IfElseExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 11;

        if (a>b){
            System.out.println(a + " is greater than " + b);
        }
        else {
            System.out.println(b + " is greater than " + a);
        }
    }

}
